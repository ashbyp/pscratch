def count_trees(col_inc, row_inc):
    grid = list(map(list, data.splitlines()))
    n_rows, n_cols = len(grid), len(grid[0])
    row, col = 0, 0
    n_trees = 0
    while True:
        row = row + row_inc
        col = (col + col_inc) % n_cols
        if row > n_rows - 1:
            break
        if grid[row][col] == '#':
            n_trees = n_trees + 1
    return n_trees


