def part2(data: str, debug: bool = False):
    pairs = {
        '{': '}',
        '[': ']',
        '(': ')',
        '<': '>',
    }
    pts = {
        ')': 1,
        ']': 2,
        '}': 3,
        '>': 4
    }

    incomplete = []

    for lineno, line in enumerate(data.splitlines()):
        stack = []
        for c in line:
            if c in ('{', '[', '<', '('):
                stack.append(c)
            else:
                open = stack.pop()
                if c != pairs[open]:
                    break
        else:
            incomplete.append(list(reversed([pairs[s] for s in stack])))

    scores = []
    for i in incomplete:
        t = 0
        for c in i:
            t = t * 5 + pts[c]
        scores.append(t)

    print(sorted(scores)[len(scores) // 2])


