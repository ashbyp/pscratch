def find_start(g, nr, nc):
    for r in range(nr):
        for c in range(nc):
            if g[r][c] == '^':
                return r, c
    return None


UP = (-1, 0)
RIGHT = (0, 1)
DOWN = (1, 0)
left = (0, -1)
DIRS = (UP, RIGHT, DOWN, left)


def part2(data: str, _debug: bool = False):
    grid = [list(l) for l in data.splitlines()]
    n_rows = len(grid)
    n_cols = len(grid[0])
    start_r, start_c = find_start(grid, n_rows, n_cols)
    stops = 0

    for R in range(n_rows):
        for C in range(n_cols):

            if grid[R][C] != '#' and grid[R][C] != '^':
                grid[R][C] = '#'

                seen = set()
                cp = 0
                d = DIRS[cp]
                r, c = start_r, start_c
                while True:

                    seen.add((r, c, d))
                    d = DIRS[cp]
                    nr = r + d[0]
                    nc = c + d[1]

                    if not (0 <= nr < n_rows and 0 <= nc < n_cols):
                        break
                    if grid[nr][nc] == '#':
                        cp = (cp + 1) % len(DIRS)
                    else:
                        r, c = nr, nc
                        if (r, c, d) in seen:
                            stops += 1
                            break

                grid[R][C] = '.'

    print('Stops', stops)


